fun main() {
    // Kotlin Practice, Beginner to Advanced Topics
    /*
         A simple print statement, declaring and initializing different variable
         types in the Kotlin syntax. String, Boolean and Integer are being output
         to the console.
     */

    // Variable Declaration and Initializations
    var stringSentence: String = "String Sentence"
    var booleanType: Boolean = true
    var intType: Int = 45

    // Declaration of variable name and type without an initialization statement
    var intAlso: Int

    // Variable initialization with a value
    intAlso = 100

    // Console print output of all variables
    println(stringSentence)
    println(booleanType)
    println(intType)
    println(intAlso)
}