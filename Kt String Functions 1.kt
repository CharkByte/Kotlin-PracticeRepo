/*
   Kotlin string library functions. Using capitalize & length check functions
*/

fun main() {
    val number = "twenty four"

    println("// Kotlin string library function that checks length of string:")
    println( " '$number' is " + number.length + " characters long")
    println("// Kotlin string library function that capitalizes first char of string:")
    println(number.capitalize())
    println("// Original string data, remains unchanged:")
    print(number)
}